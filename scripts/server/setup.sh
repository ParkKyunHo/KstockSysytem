#!/bin/bash
# ============================================================
# K_stock_trading 서버 초기 설정
# ============================================================
# 사용법: ./setup.sh
#
# 실행 내용:
# 1. 시스템 업데이트
# 2. Python 3.11 설치
# 3. 필수 패키지 설치
# 4. 타임존 설정 (KST)
# 5. 디렉토리 구조 생성
# ============================================================

set -e

echo "============================================================"
echo " K_stock_trading 서버 초기 설정"
echo "============================================================"
echo

# ============================================================
# 1. 시스템 업데이트
# ============================================================
echo "[1/5] 시스템 업데이트..."
sudo apt update && sudo apt upgrade -y

# ============================================================
# 2. Python 3.11 설치
# ============================================================
echo
echo "[2/5] Python 3.11 설치..."
sudo apt install -y software-properties-common
sudo add-apt-repository -y ppa:deadsnakes/ppa
sudo apt update
sudo apt install -y python3.11 python3.11-venv python3.11-dev

# 기본 Python 설정
sudo update-alternatives --install /usr/bin/python3 python3 /usr/bin/python3.11 1

echo "       Python 버전: $(python3 --version)"

# ============================================================
# 3. 필수 패키지 설치
# ============================================================
echo
echo "[3/5] 필수 패키지 설치..."
sudo apt install -y git curl wget htop unzip tree

# ============================================================
# 4. 타임존 설정
# ============================================================
echo
echo "[4/5] 타임존 설정 (Asia/Seoul)..."
sudo timedatectl set-timezone Asia/Seoul
echo "       현재 시간: $(date)"

# ============================================================
# 5. 디렉토리 구조 생성
# ============================================================
echo
echo "[5/5] 디렉토리 구조 생성..."
mkdir -p ~/K_stock_trading/{releases,shared/{logs,data},scripts}

echo "       구조:"
tree ~/K_stock_trading -L 2

echo
echo "============================================================"
echo " 초기 설정 완료!"
echo "============================================================"
echo
echo " 다음 단계:"
echo " 1. 로컬에서 first-deploy.bat 실행"
echo " 2. .env 파일 설정: nano ~/K_stock_trading/shared/.env"
echo " 3. 서비스 시작 확인"
echo
