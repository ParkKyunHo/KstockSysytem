#!/bin/bash
# ============================================================
# K_stock_trading 헬스체크
# ============================================================
# 사용법: ./health-check.sh
# 종료 코드:
#   0 - 정상
#   1 - 비정상
# ============================================================

BASE_DIR="$HOME/K_stock_trading"
CURRENT_LINK="$BASE_DIR/current"

echo "============================================================"
echo " K_stock_trading 헬스체크"
echo "============================================================"
echo

ERRORS=0

# ============================================================
# 1. 서비스 상태 확인
# ============================================================
echo "[1/4] 서비스 상태..."
if systemctl is-active --quiet k-stock-trading; then
    echo "       ✅ 서비스 실행 중"
else
    echo "       ❌ 서비스 중지됨"
    ERRORS=$((ERRORS + 1))
fi

# ============================================================
# 2. 프로세스 확인
# ============================================================
echo
echo "[2/4] 프로세스 확인..."
PYTHON_PROCS=$(pgrep -f "python.*launcher.py" | wc -l)
if [ "$PYTHON_PROCS" -gt 0 ]; then
    echo "       ✅ Python 프로세스 실행 중 ($PYTHON_PROCS개)"
else
    echo "       ❌ Python 프로세스 없음"
    ERRORS=$((ERRORS + 1))
fi

# ============================================================
# 3. 최근 로그 확인 (에러 여부)
# ============================================================
echo
echo "[3/4] 최근 로그 확인..."
RECENT_ERRORS=$(sudo journalctl -u k-stock-trading --since "2 minutes ago" 2>/dev/null | grep -i "error\|exception\|critical" | wc -l)
if [ "$RECENT_ERRORS" -eq 0 ]; then
    echo "       ✅ 최근 2분 내 에러 없음"
else
    echo "       ⚠️  최근 에러 $RECENT_ERRORS건 (경고)"
    # 에러가 있어도 서비스가 실행 중이면 통과
fi

# ============================================================
# 4. 버전 확인
# ============================================================
echo
echo "[4/4] 버전 확인..."
if [ -f "$CURRENT_LINK/VERSION" ]; then
    VERSION=$(cat "$CURRENT_LINK/VERSION")
    echo "       ✅ 버전: $VERSION"
else
    echo "       ❌ VERSION 파일 없음"
    ERRORS=$((ERRORS + 1))
fi

# ============================================================
# 결과
# ============================================================
echo
echo "============================================================"
if [ "$ERRORS" -eq 0 ]; then
    echo " ✅ 헬스체크 통과"
    echo "============================================================"
    exit 0
else
    echo " ❌ 헬스체크 실패 ($ERRORS개 오류)"
    echo "============================================================"
    exit 1
fi
